package com.example.basics.operators;

public class Op3_relational_operators {
    public static void main(String[] args) {

        boolean b1 = 5 == 3; // false
        System.out.println(b1);

        boolean b2 = 7 == 7; // true

        boolean b3 = 7 != 7; // false
        boolean b4 = 7 != 5; // true
    }
}
