package com.example.questions;
/**
 *
 *
 * Pytanie: Dlaczego mówi się, że Java to język 'zapisz raz i uruchom wszędzie'?
 * Odpowiedź:
 * Filozofia 'zapisz raz i uruchom wszędzie' (WORA) jest możliwa w Javie dzięki
 * użyciu Java Virtual Machine (JVM). Kod źródłowy Java jest kompilowany do
 * uniwersalnego formatu bajtkodu, który może być następnie uruchamiany na
 * dowolnej maszynie wirtualnej Java, niezależnie od architektury sprzętowej
 * czy systemu operacyjnego. To sprawia, że aplikacje Java są przenośne i mogą
 * być uruchamiane na różnych platformach bez konieczności modyfikacji kodu.
 */

public class Java_004_why_java_can_run_anywhere {
    public static void main(String[] args) {
        // Demonstracja uniwersalności bajtkodu Java
        System.out.println("Java umożliwia pisanie aplikacji 'zapisz raz, "
                + "uruchom wszędzie' dzięki użyciu JVM.");
    }
}

